const MESSAGES: Record<string, WidgetMessages> = { en, es, fr, de, pt, it, nl, zh, ja, ko, hi, ar };

/** Returns authored copy for a language, falling back to English. */
export function getMessages(language: string): WidgetMessages {
  return MESSAGES[language] || MESSAGES[language.split("-")[0]] || en;
}

export function hasAuthoredCopy(language: string): boolean {
  return Boolean(MESSAGES[language] || MESSAGES[language.split("-")[0]]);
}
